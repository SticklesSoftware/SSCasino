namespace SSCasino.Models
{
    public class InfoPanelEntryLine
    {
        #region Properties
        //================================================================================================================
        //================================================================================================================
        public string EntryLineHeader { get; set; }
        public string EntryLineBody { get; set; }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Properties

        #region Constructors
        //================================================================================================================
        //================================================================================================================

        public InfoPanelEntryLine()
        //================================================================================================================
        // Default constructor
        //================================================================================================================
        {
        }

        public InfoPanelEntryLine(string entryLineHeader, string entryLineBody)
        //================================================================================================================
        // Alternate constructor
        //
        // Parameters
        //      entryLineHeader: Header text for the entry line
        //      entryLineBody:   Body text for the entry line
        //================================================================================================================
        {
            // Set properties
            EntryLineHeader = entryLineHeader;
            EntryLineBody = entryLineBody;
        }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Constructors
    }
}
