using System.Collections.Generic;

namespace SSCasino.Models
{
    public class ShuffledPackage
    {
        #region Properties
        //================================================================================================================
        //================================================================================================================
        public CardPack CardPack { get; set; }
        public ICollection<ShuffleResult> ShuffleResults { get; set; }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Properties

        #region Constructors
        //================================================================================================================
        //================================================================================================================

        public ShuffledPackage()
        //================================================================================================================
        // Default constructor
        //================================================================================================================
        {
            // Create the collection for the shuffling results
            ShuffleResults = new HashSet<ShuffleResult>();
        }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Constructors
    }
}
