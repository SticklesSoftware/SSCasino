namespace SSCasino.Models
{
    public class FormHeader
    {
        #region Constants
        //================================================================================================================
        //================================================================================================================

        // Default properties
        private const string DEF_FORM_IMAGE = "~/Content/Images/Forms/DefFormImage.png";
        private const string DEF_FORM_TITLE = "NO Title Specified";
        private const string DEF_FORM_STYLES = "ssc_SmallForm";

        //================================================================================================================
        //================================================================================================================
        #endregion  // Constants

        #region Properties
        //================================================================================================================
        //================================================================================================================

        public string FormImage { get; set; }
        public string FormTitle { get; set; }
        public string FormStyles { get; set; }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Properties

        #region Constructors
        //================================================================================================================
        //================================================================================================================

        public FormHeader()
        //================================================================================================================
        // Default Constructor
        //================================================================================================================
        {
            // Assign default properties
            FormImage = DEF_FORM_IMAGE;
            FormTitle = DEF_FORM_TITLE;
            FormStyles = DEF_FORM_STYLES;
        }

        public FormHeader(string formIamge, string formTitle, string styleClass)
        //================================================================================================================
        // Alternate Constructor
        //
        // Parameters
        //      formImage:  Path to the image for the form
        //      formTitle:  Title for the form
        //      styleClass: Style classes to apply
        //================================================================================================================
        {
            // Assign properties based on the given parameters
            FormImage = formIamge;
            FormTitle = formTitle;
            FormStyles = styleClass;
        }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Constructors
    }
}