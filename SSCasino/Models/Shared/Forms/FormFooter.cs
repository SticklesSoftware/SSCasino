namespace SSCasino.Models
{
    public class FormFooter
    {
        #region Constants
        //================================================================================================================
        //================================================================================================================

        // Default properties
        private const string DEF_FORM_STYLES = "ssc_SmallForm";

        //================================================================================================================
        //================================================================================================================
        #endregion  // Constants

        #region Properties
        //================================================================================================================
        //================================================================================================================

        public string FormStyles { get; set; }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Properties

        #region Constructors
        //================================================================================================================
        //================================================================================================================

        public FormFooter()
        //================================================================================================================
        // Default Constructor
        //================================================================================================================
        {
            // Assign default properties
            FormStyles = DEF_FORM_STYLES;
        }

        public FormFooter(string styleClass)
        //================================================================================================================
        // Alternate Constructor
        //
        // Parameters
        //      formImage: Path to the image for the form
        //      formTitle: Title for the form
        //================================================================================================================
        {
            // Assign properties based on the given parameters
            FormStyles = styleClass;
        }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Constructors
    }
}