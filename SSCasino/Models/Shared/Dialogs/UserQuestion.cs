namespace SSCasino.Models
{
    public class UserQuestion
    {
        #region Properties
        //================================================================================================================
        //================================================================================================================

        public string Question { get; set; }
        public string YesFunctionName { get; set; }
        public string NoFunctionName { get; set; }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Properties

        #region Constructors
        //================================================================================================================
        //================================================================================================================

        public UserQuestion()
        //================================================================================================================
        // Default constructor
        //================================================================================================================
        {
        }

        public UserQuestion(string userQuestion, string yesFunctionName, string noFunctionName)
        //================================================================================================================
        // Alternate constructor
        //
        // Parameters
        //      userQuestion:    Question text for the user
        //      yesFunctionName: Function to execute when user presses YES
        //      noFunctionName:  Function to execute when user presses NO
        //================================================================================================================
        {
            // Assign properties based on parameters
            Question = userQuestion;
            YesFunctionName = yesFunctionName;
            NoFunctionName = noFunctionName;
        }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Constructors
    }
}