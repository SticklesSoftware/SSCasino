using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;

namespace SSCasino.Models
{
    public class CardPack
    {
        #region Properties
        //================================================================================================================
        //================================================================================================================
        [Key]
        public int CardPackId { get; set; }
        public string CardPackName { get; set; }
        public string CardBackImage { get; set; }
        public string CardFrontImage { get; set; }
        public int DefaultPack { get; set; }

        public List<PlayingCard> CardDeck { get; set; }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Properties

        #region Constructors
        //================================================================================================================
        //================================================================================================================

        public CardPack()
        //================================================================================================================
        // Default constructor
        //================================================================================================================
        {
            // Create the playing cards list
            CardDeck = new List<PlayingCard>();
        }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Constructors
    }
}
