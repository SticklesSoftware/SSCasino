namespace SSCasino.Models
{
    public class YouTubeCategory
    {
        #region Properties
        //================================================================================================================
        //================================================================================================================
        public string SessionKey { get; set; }
        public string SearchPhrase { get; set; }
        public string Caption { get; set; }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Properties

        #region Constructors
        //================================================================================================================
        //================================================================================================================

        public YouTubeCategory()
        //================================================================================================================
        // Default constructor
        //================================================================================================================
        {
        }

        public YouTubeCategory(string sessionKey, string searchPhrase, string caption)
        //================================================================================================================
        // Alternate constructor
        //
        // Properties
        //      sessionKey:   Every YouTube search uses a unique session key for local storage
        //      searchPhrase: YouTube search phrase
        //      caption:      Category caption
        //================================================================================================================
        {
            // Default properties
            SessionKey = sessionKey;
            SearchPhrase = searchPhrase;
            Caption = caption;
        }

        //================================================================================================================
        //================================================================================================================
        #endregion  // Constructors
    }
}
